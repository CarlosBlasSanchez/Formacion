package com.ejercicios;

public class Ejercicio5_10 {

	public static void main(String[] args) {
		int numCarateres=8;
		char caracter='*';
		for (int i=0; i<numCarateres; i++) {
			for (int j=0; j<numCarateres; j++) {
				System.out.print(caracter);
			}
			System.out.println();
		}

	}

}
